package com.sh.exercise;

// 문자와 숫자를 출력한다.
class SampleP2 {                                                // SampleP2 클래스 정의 시작
    public static void main(String[] args) {                    // main() 메소드 정의 시작
        System.out.println('A');                                // System.out.println() 메소드 호출하여 문자 'A' 출력
        System.out.println("Java 세상에 오신 것을 환영합니다!");     // System.out.println() 메소드 호출하여 문자 'A' 출력
        System.out.println(123);                                // System.out.println() 메소드 호출하여 문자 'A' 출력
    }                                                           // main() 메소드 정의 끝
}                                                               // SampleP2 클래스 정의 끝
