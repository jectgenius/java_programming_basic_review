package com.sh.exercise;

// 화면에 문자를 출력하는 코드
class SampleP1 {                                             // Sample1 클래스 정의 시작
    public static void main(String[] args) {                // main() 메소드 정의 시작
        System.out.println("Java 세상에 오신 것을 환영합니다.");// System.out.println() 메소드 호출하여 "Java 세상에 오신 것을 환영합니다." 문자열 출력
        System.out.println("Java를 시작합시다!");             // System.out.println() 메소드 호출하여 "Java를 시작합시다!" 문자열 출력
    }                                                       // main() 메소드 정의 끝
}                                                           // Sample1 클래스 정의 끝
